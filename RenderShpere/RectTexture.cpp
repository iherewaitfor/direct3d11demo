//--------------------------------------------------------------------------------------
// Render a frame
//--------------------------------------------------------------------------------------
void Render()
{
    // Update our time
    static float t = 0.0f;
    //if( g_driverType == D3D_DRIVER_TYPE_REFERENCE )
    //{
    //    t += ( float )XM_PI * 0.0125f;
    //}
    //else
    //{
    //    static DWORD dwTimeStart = 0;
    //    DWORD dwTimeCur = GetTickCount();
    //    if( dwTimeStart == 0 )
    //        dwTimeStart = dwTimeCur;
    //    t = ( dwTimeCur - dwTimeStart ) / 1000.0f;
    //}

    // Rotate cube around the origin
    g_World = XMMatrixRotationY( t );

    // Modify the color
    g_vMeshColor.x = ( sinf( t * 1.0f ) + 1.0f ) * 0.5f;
    g_vMeshColor.y = ( cosf( t * 3.0f ) + 1.0f ) * 0.5f;
    g_vMeshColor.z = ( sinf( t * 5.0f ) + 1.0f ) * 0.5f;

    //
    // Clear the back buffer
    //
    float ClearColor[4] = { 0.0f, 0.125f, 0.3f, 1.0f }; // red, green, blue, alpha
    g_pImmediateContext->ClearRenderTargetView( g_pRenderTargetView, ClearColor );

    //
    // Clear the depth buffer to 1.0 (max depth)
    //
    g_pImmediateContext->ClearDepthStencilView( g_pDepthStencilView, D3D11_CLEAR_DEPTH, 1.0f, 0 );

    //
    // Update variables that change once per frame
    //
    CBChangesEveryFrame cb;
    cb.mWorld = XMMatrixTranspose( g_World );
    cb.vMeshColor = g_vMeshColor;
    g_pImmediateContext->UpdateSubresource( g_pCBChangesEveryFrame, 0, NULL, &cb, 0, 0 );

    //
    // Render the cube
    //
    g_pImmediateContext->VSSetShader( g_pVertexShader, NULL, 0 );
    g_pImmediateContext->VSSetConstantBuffers( 0, 1, &g_pCBNeverChanges );
    g_pImmediateContext->VSSetConstantBuffers( 1, 1, &g_pCBChangeOnResize );
    g_pImmediateContext->VSSetConstantBuffers( 2, 1, &g_pCBChangesEveryFrame );
    g_pImmediateContext->PSSetShader( g_pPixelShader, NULL, 0 );
    g_pImmediateContext->PSSetConstantBuffers( 2, 1, &g_pCBChangesEveryFrame );
    g_pImmediateContext->PSSetShaderResources( 0, 1, &g_pTextureRV );
    g_pImmediateContext->PSSetSamplers( 0, 1, &g_pSamplerLinear );
    g_pImmediateContext->DrawIndexed(g_indexCount, 0, 0);

    //
    // Present our back buffer to our front buffer
    //
    g_pSwapChain->Present( 0, 0 );
}


bool InitializeSphereBuffers(ID3D11Device* device, float radius, int numSlices, int numStacks) {
	D3D11_BUFFER_DESC vertexBufferDesc, indexBufferDesc;
	D3D11_SUBRESOURCE_DATA vertexData, indexData;
	HRESULT result;

	float m_Radius = radius;
	int m_NumSlices = numSlices; //经度
	int m_NumStacks = numStacks; //纬度

	std::vector<VertexType> vertices;
	std::vector<int> indices;
	// numSlices; //经度
	MakeSphere(vertices, indices, radius, numSlices, numStacks);

	int m_vertexCount = (int)vertices.size();
	int m_indexCount = (int)indices.size();
	g_indexCount = m_indexCount;


	// 设置顶点缓冲描述
	vertexBufferDesc.Usage = D3D11_USAGE_DEFAULT;
	vertexBufferDesc.ByteWidth = sizeof(VertexType) * m_vertexCount;
	vertexBufferDesc.BindFlags = D3D11_BIND_VERTEX_BUFFER;
	vertexBufferDesc.CPUAccessFlags = 0;
	vertexBufferDesc.MiscFlags = 0;
	vertexBufferDesc.StructureByteStride = 0;

	// 指向保存顶点数据的临时缓冲.
	vertexData.pSysMem = &vertices[0];
	vertexData.SysMemPitch = 0;
	vertexData.SysMemSlicePitch = 0;

	// 创建顶点缓冲.
	result = device->CreateBuffer(&vertexBufferDesc, &vertexData, &g_pVertexBuffer);
	if (FAILED(result))
	{
		return false;
	}

	// 设置索引缓冲描述.
	indexBufferDesc.Usage = D3D11_USAGE_DEFAULT;
	indexBufferDesc.ByteWidth = sizeof(int) * m_indexCount;
	indexBufferDesc.BindFlags = D3D11_BIND_INDEX_BUFFER;
	indexBufferDesc.CPUAccessFlags = 0;
	indexBufferDesc.MiscFlags = 0;
	indexBufferDesc.StructureByteStride = 0;

	// 指向存临时索引缓冲.
	indexData.pSysMem = &indices[0];
	indexData.SysMemPitch = 0;
	indexData.SysMemSlicePitch = 0;

	// 创建索引缓冲.
	result = device->CreateBuffer(&indexBufferDesc, &indexData, &g_pIndexBuffer);
	if (FAILED(result))
	{
		return false;
	}
	return true;

}

void MakeSphere(VertexList& vertices, IndexList& indices, float radius, int numSlices, int numStacks) {
	float phiStep = PI / numStacks;

	int numRings = numStacks - 1;

	// 对于每个纬度环，计算顶点.
	for (int i = 1; i <= numRings; ++i)
	{
		float phi = i * phiStep;

		// 环上的顶点
		float thetaStep = 2.0f*PI / numSlices;
		for (int j = 0; j <= numSlices; ++j)
		{
			float theta = j * thetaStep;

			VertexType v;

			// 球坐标到笛卡尔坐标的转化
			v.position.x = radius * sinf(phi)*cosf(theta);
			v.position.y = radius * cosf(phi);
			v.position.z = radius * sinf(phi)*sinf(theta);

			D3DXVec3Normalize(&v.normal, &v.position);

			//球的纹理坐标
			v.texture.x = theta / (2.0f*PI);
			v.texture.y = phi / PI;

			vertices.push_back(v);
		}
	}

	// 球的极点: 会出现纹理坐标扭曲
	VertexType t1;
	t1.position = D3DXVECTOR3(0.0f, -radius, 0.0f);
	t1.normal = D3DXVECTOR3(0.0f, -1.0f, 0.0f);
	t1.texture = D3DXVECTOR2(0.0f, 1.0f);

	vertices.push_back(t1);

	t1.position = D3DXVECTOR3(0.0f, radius, 0.0f);
	t1.normal = D3DXVECTOR3(0.0f, 1.0f, 0.0f);
	t1.texture = D3DXVECTOR2(0.0f, 0.0f);

	vertices.push_back(t1);

	int northPoleIndex = (int)vertices.size() - 1;
	int southPoleIndex = (int)vertices.size() - 2;

	int numRingVertices = numSlices + 1;

	// 计算索引(不考虑极点)
	for (int i = 0; i < numSlices - 2; ++i)
	{
		for (int j = 0; j < numSlices; ++j)
		{
			indices.push_back(i*numRingVertices + j);
			indices.push_back(i*numRingVertices + j + 1);
			indices.push_back((i + 1)*numRingVertices + j);

			indices.push_back((i + 1)*numRingVertices + j);
			indices.push_back(i*numRingVertices + j + 1);
			indices.push_back((i + 1)*numRingVertices + j + 1);
		}
	}

	//北极点索引
	for (int i = 0; i < numSlices; ++i)
	{
		indices.push_back(northPoleIndex);
		indices.push_back(i + 1);
		indices.push_back(i);
	}

	//南极点索引
	int baseIndex = (numRings - 1)*numRingVertices;
	for (int i = 0; i < numSlices; ++i)
	{
		indices.push_back(southPoleIndex);
		indices.push_back(baseIndex + i);
		indices.push_back(baseIndex + i + 1);
	}
}

void RenderSphereBuffers(ID3D11DeviceContext* deviceContext)
{
	unsigned int stride;
	unsigned int offset;


	// 设置顶点缓冲跨度和偏移.
	stride = sizeof(VertexType);
	offset = 0;

	//在input assemberl阶段绑定顶点缓冲，以便能够被渲染
	deviceContext->IASetVertexBuffers(0, 1, &g_pVertexBuffer, &stride, &offset);

	//在input assemberl阶段绑定索引缓冲，以便能够被渲染
	deviceContext->IASetIndexBuffer(g_pIndexBuffer, DXGI_FORMAT_R32_UINT, 0);

	// 设置体元语义，渲染三角形列表.
	deviceContext->IASetPrimitiveTopology(D3D11_PRIMITIVE_TOPOLOGY_TRIANGLELIST);

	return;
}